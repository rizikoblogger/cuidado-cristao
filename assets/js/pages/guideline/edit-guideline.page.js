  methods: {
    remove: function (classroom) {
      this.guideline.tos = this.guideline.tos.filter(to=>to.id!==classroom.id)
    },

    addClassroom: function (classroom) {
      this.guideline.tos.push(classroom)
    },

    save: function () {
      Cloud
        .saveGuideline
        .with(this.guideline)
        .then((result)=>{
          this.guideline = result.guideline
          alert(`Saved successfully!`)
          this.goto(`/guideline/search-guideline`)
        })
        .catch((err)=>{
          console.log(err)
          alert(`Sorry! Something went wrong.`)
        })
    }
  }
